public class switchCase {
    public static void main(String[] args){
        String Ne = "Meo";
        switch (Ne){
            case"Meo":
                System.out.println("Con Meo");
                break;
            case"Cho":
                System.out.println("Con Cho");
                break;
            default:
                System.out.println("Con Ca");
        }
    }
}
