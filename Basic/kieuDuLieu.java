public class kieuDuLieu {
    public static void main(String[] args) {
        int a = 1;
        //In rôi mới tăng
        System.out.println(a++);
        //Tăng ròi mới in
        System.out.println(++a);
        //Cộng 1 và in ra rô mới tăng
        System.out.println(a+++1);
        //Tăng rồi mới cộng 1 và in ra
        System.out.println(++a+1);
        //thaotac bit integer, long, int, short, char và byte.
    }
}
